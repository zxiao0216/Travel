<template>
    <div class="container" @click="handleGallaryClick">
        <div class="wrapper">
            <swiper :options="swiperOptions">
                <swiper-slide v-for="(item,index) of imgs" :key="index">
                    <img class="gallery-image" :src="item">
                </swiper-slide>
            <!-- 轮播图下边的小点 -->
            <div class="swiper-pagination"  slot="pagination"></div>
            </swiper>
        </div>
    </div>
</template>

<script>
export default {
    name:'CommonGallary',
    props:{
        imgs:{
            type:Array,
            default(){
                return[]
            }
        }
    },
    data(){
        return{
            swiperOptions:{
                pagination:'.swiper-pagination',
                paginationType:'fraction',
                // 下边这两个是解决加入图片点击显示轮播图以后，宽度变化，轮播图有问题。
                observeParents:true,
                observer:true
            }
        }
    },
    methods:{
        handleGallaryClick(){
            this.$emit('close')
        }
    }
}
</script>

